def add(first, second):
    result = first + second
    print("Your answer is {0}.".format(result))

def subtract(first, second):
    result = first - second
    print("Your answer is {0}.".format(result))


def multiply(first, second):
    result = first * second
    print("Your answer is {0}.".format(result))


def divide(first, second):
    result = first / second
    print("Your answer is {0}.".format(result))
